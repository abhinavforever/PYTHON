class calculation:
    def __init__(self, a, b):
        self.first_no = a
        self.second_no = b

    def arithoperations(self):
        print("addition:", self.first_no+self.second_no)
        print("subtraction:", self.first_no-self.second_no)
        print("multiplication:", self.first_no*self.second_no)
        print("division:", self.first_no/self.second_no)
